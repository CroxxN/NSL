def draw_info_text(image, brect, handedness, hand_sign_text
                   ):
    cv.rectangle(image, (brect[0], brect[1]), (brect[2], brect[1] - 12),
                 (0, 0, 0), -1)

    info_text = handedness.classification[0].label[0:]
    if hand_sign_text != "":
        info_texts = info_text + ':' + hand_sign_text
    if info_text=="Right":
            # Calculate the width and height of the text
        (w, h), _ = cv.getTextSize(handedness.classification[0].label[0:], cv.FONT_HERSHEY_COMPLEX, 0.6, 1)

        # Calculate the position of the top-right corner of the text rectangle
        x = image.shape[1] -w -255
        y = h + 100

        # Draw the rectangle behind the text
        cv.rectangle(image, (x, y), (x+310 , y + h+19), (127, 127, 127), -1)

        cv.putText(image, info_texts, (x , y + h+15), cv.FONT_HERSHEY_COMPLEX, 1, (255, 255, 255), 1, cv.LINE_AA)

    if info_text=="Left":
           # Calculate the width and height of the text
        (w, h), _ = cv.getTextSize(handedness.classification[0].label[0:], cv.FONT_HERSHEY_COMPLEX, 0.6, 1)

        # Calculate the position of the top-right corner of the text rectangle
        x = 0
        y = h + 100

        # Draw the rectangle behind the text
        cv.rectangle(image, (x, y), (x + 300, y + h + 19), (127, 127, 127), -1)

        cv.putText(image, info_texts, (x + 10, y + h+15), cv.FONT_HERSHEY_COMPLEX, 1, (255, 255, 255), 1, cv.LINE_AA)
    
    return image

# This is where we display text in upper-left corner 
def draw_info(image, mode):

    mode_string = ['Logging Key Point', 'Logging Point History']
    if 1 <= mode <= 2:
        cv.putText(image, "MODE:" + mode_string[mode - 1], (10, 90),
                   cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1,
                   cv.LINE_AA)
    return image

